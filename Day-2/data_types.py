# Data Types

# String
print("Hello"[4]) # In Python, the first character is always 0 and you can print specific characters from a string by using the []
print("123"[1])
print("123" + "345")

# Integer
print(123 + 345)
print(123_456_789) # In Python, we can use underscore to make the number more readable

# Float

pi = 3.14159
print(pi)

# Boolean

is_active = True
print(is_active)
